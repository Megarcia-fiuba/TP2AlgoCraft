package algocraft.juego;

public class Posicion {
	
	private int posicion_x;
	private int posicion_y;

	public Posicion(int i, int j) {
		this.posicion_x = i;
		this.posicion_y = j;
	}
	
	public int getPosicionX() {
		return posicion_x;
	}
	
	public int getPosicionY() {
		return posicion_y;
	}
}


